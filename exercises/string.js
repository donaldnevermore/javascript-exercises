let a = "Hello world!";
console.log(a.slice(3)); // 从第3个字符（从第0个开始数）开始到末尾
console.log(a.slice(3, 7)); // 从第3个字符到第6个字符,不包括上限
console.log(a); // 不会改变原来的字符串

console.log(a.substring(3));
console.log(a.substring(3, 7));
console.log(a);
